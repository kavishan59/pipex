#include <unistd.h>

void	ft_putstr_fd(char *s, int fd)
{
	int	i;

	if (!s || fd > 1024 || fd < 0)
		return ;
	i = 0;
	while (s[i])
	{
		write(fd, &s[i], 1);
		i++;
	}
}
