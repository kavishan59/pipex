#include <unistd.h>

void	ft_putnbr_fd(int n, int fd)
{
	char	c;

	if (fd < 0 || fd > 1024)
		return ;
	if (n == -2147483648)
		write(fd, "-2147483648", 11);
	else
	{	
		if (n < 0)
		{
			n = n * -1;
			write(fd, "-", 1);
		}
		if (n > 9)
			ft_putnbr_fd(n / 10, fd);
		n = n % 10;
		c = n + 48;
		write(fd, &c, 1);
	}
}
